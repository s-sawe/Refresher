public class whileloop {
    public static void main(String[] args){

        //while loop
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }


        //do...while loop
        int s = 0;
        do{
            System.out.println("The Value of the Do...While Loop is " +s); //this was just a distinguishing string.
            s++;
        }while(s<5);
    }
}
